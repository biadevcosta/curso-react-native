console.log(x -= y) // mesma coisa que x = x - y
console.log(x *= y) // mesma coisa que x = x * y
console.log(x /= y) //mesma coisa que x = x / y
console.log(x %= y) //mesma coisa que x = x % y
console.log(x **= y)//mesma coisa que x = x ** y

